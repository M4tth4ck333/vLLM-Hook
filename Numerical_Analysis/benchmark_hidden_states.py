    os.environ.setdefault("VLLM_USE_V1", "1")
    os.environ.setdefault("VLLM_WORKER_MULTIPROC_METHOD", "spawn")

    if args.async_save:
        os.environ["VLLM_HOOK_ASYNC_SAVE"] = "1"
    if args.safetensors:
        os.environ["VLLM_HOOK_USE_SAFETENSORS"] = "1"
    if args.use_shm:
        os.environ["VLLM_HOOK_USE_SHM"] = "1"
    if args.shm_persist:
        os.environ["VLLM_HOOK_SHM_PERSIST"] = "1"

    if args.sweep_grid:
        rows = run_grid_sweep(
            dry_run=args.dry_run,
            hook_dir=args.hook_dir,
            skip_hook=args.skip_hook,
            skip_native=args.skip_native,
        )
        if not args.dry_run:
            _save_csv(rows, args.output)

    else:
        hook_results   = _empty_results()
        native_results = _empty_results()

        if not args.skip_hook:
            hook_results, *_ = run_hook_benchmark(dry_run=args.dry_run, hook_dir=args.hook_dir)

        if not args.skip_native:
            native_results = run_native_benchmark(dry_run=args.dry_run)

        if not args.dry_run:
            _print_table(hook_results, native_results)
