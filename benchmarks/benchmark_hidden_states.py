"""
Benchmark: vLLM-Hook (probe_hidden_states) vs. Native vLLM (ExampleHiddenStatesConnector)

Both approaches extract hidden states from the same layers of Qwen/Qwen2-1.5B-Instruct.

Metrics:
  - Prefill latency        : time for the prefill pass only (hooks active for vLLM-Hook;
                             full generate() call for native vLLM which is prefill-only)
  - Total latency          : prefill latency + time to load/read extracted artifacts
  - Peak GPU memory        : torch.cuda.max_memory_allocated() over the generation window
  - Artifact size          : total bytes written to disk per run (all prompts combined)

Usage:
  # On a compute node with a GPU:
  export VLLM_USE_V1=1
  export VLLM_WORKER_MULTIPROC_METHOD=spawn
  python benchmarks/benchmark_hidden_states.py [--dry-run]

  --dry-run  : verify shapes and file paths without timing (no warm-up, 1 rep)

Sweep flags (items 1-4 from THINGS_TO_ADD.md):
  --sweep-layers      vary number of layers extracted
  --sweep-prompt-len  vary prompt token length
  --sweep-token-mode  vary last_token vs all_tokens extraction mode
  --sweep-grid        full 3D grid: all combinations of layers × prompt_len × token_mode
  --output FILE       write CSV results to FILE (default: benchmark_results.csv)
"""

