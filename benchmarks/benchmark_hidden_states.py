def run_hook_benchmark(dry_run=False, hook_dir=None, *,
                       layers=None, max_tokens=None, token_mode=None, prompts=None):
    """
    Run the vLLM-Hook benchmark.

    layers, token_mode: if provided, a temp config is written and used instead of
                        CONFIG_FILE. Both must be provided together.
    max_tokens: overrides MAX_TOKENS for SamplingParams.
    prompts: overrides global PROMPTS list.
    """
    print("\n[vLLM-Hook] Initializing...")
    sys.path.insert(0, str(PROJECT_ROOT / "vllm_hook_plugins"))

    from vllm_hook_plugins import register_plugins
    from vllm_hook_plugins.hook_llm import HookLLM

    register_plugins()

    _max_tokens = max_tokens if max_tokens is not None else MAX_TOKENS
    _prompts = prompts if prompts is not None else PROMPTS

    # Build config path — write temp config if layers/mode overrides requested
    _temp_cfg = None
    if layers is not None or token_mode is not None:
        _layers = layers if layers is not None else TARGET_LAYERS
        _mode = token_mode if token_mode is not None else "last_token"
        _temp_cfg = _write_temp_config(CONFIG_FILE, _layers, _mode)
        cfg_path = _temp_cfg
    else:
        cfg_path = CONFIG_FILE

    try:
        init_kwargs = dict(
            model=MODEL_ID,
            worker_name="probe_hidden_states",
            analyzer_name="hidden_states",
            config_file=cfg_path,
            download_dir=DOWNLOAD_DIR,
            dtype=torch.float16,
            enable_hook=True,
            enforce_eager=True,
            enable_prefix_caching=False,
        )
        if hook_dir is not None:
            init_kwargs["hook_dir"] = hook_dir

        llm = HookLLM(**init_kwargs)
        _hook_dir = llm._hook_dir
        print(f"[vLLM-Hook] hook_dir = {_hook_dir}")
        sampling_params = SamplingParams(temperature=0.0, max_tokens=_max_tokens)

        llm._setup_hooks(cleanup=True)

        use_shm = os.environ.get("VLLM_HOOK_USE_SHM", "0") == "1"
        shm_persist = os.environ.get("VLLM_HOOK_SHM_PERSIST", "0") == "1"
        use_safetensors = os.environ.get("VLLM_HOOK_USE_SAFETENSORS", "0") == "1"
        use_async_save = os.environ.get("VLLM_HOOK_ASYNC_SAVE", "0") == "1"

        # Determine hidden_size from config for shape assertions
        with open(cfg_path) as _f:
            _cfg_data = json.load(_f)
        _hidden_size = _cfg_data.get("hidden_size")
        _extract_mode = _cfg_data.get("hidden_states", {}).get("mode", "last_token")

        def one_run():
            run_id = str(uuid.uuid4())
            with open(llm._run_id_file, "a") as f:
                f.write(run_id + "\n")

            t0 = time.perf_counter()
            llm.llm.generate(_prompts, sampling_params)
            t1 = time.perf_counter()
            stats = llm.analyze(analyzer_spec={"reduce": "none"})
            t2 = time.perf_counter()

            if use_shm and shm_persist:
                expected_pt = os.path.join(_hook_dir, run_id, "tp_rank_0", "hidden_states.pt")
                raw = torch.load(expected_pt, map_location="cpu", weights_only=False)
                peak = raw.get("peak_gpu_mb", _gpu_used_mb())
                art_kb = _files_size_kb([expected_pt])
            elif use_shm:
                peak = stats.get("peak_gpu_mb", _gpu_used_mb())
                art_kb = 0.0
            elif use_safetensors:
                art_st = os.path.join(_hook_dir, run_id, "tp_rank_0", "hidden_states.safetensors")
                art_json = os.path.join(_hook_dir, run_id, "tp_rank_0", "hidden_states.json")
                if use_async_save:
                    deadline = time.time() + 60.0
                    while time.time() < deadline:
                        if os.path.exists(art_json) and os.path.exists(art_st):
                            break
                        time.sleep(0.01)
                try:
                    with open(art_json) as f:
                        peak = json.load(f).get("peak_gpu_mb", _gpu_used_mb())
                except (FileNotFoundError, KeyError, ValueError):
                    peak = _gpu_used_mb()
                art_kb = _files_size_kb([art_st, art_json])
            else:
                expected_pt = os.path.join(_hook_dir, run_id, "tp_rank_0", "hidden_states.pt")
                raw = torch.load(expected_pt, map_location="cpu", weights_only=False)
                peak = raw.get("peak_gpu_mb", _gpu_used_mb())
                art_kb = _files_size_kb([expected_pt])

            return t1 - t0, t2 - t0, peak, art_kb, stats

        # Warm-up
        _n_warmup = 0 if dry_run else N_WARMUP
        print(f"[vLLM-Hook] Warm-up ({_n_warmup} run(s))...")
        for _ in range(_n_warmup):
            one_run()

        # Timed reps
        _n_reps = 1 if dry_run else N_REPS
        results = {"gen_lat": [], "total_lat": [], "peak_mem": [], "art_kb": []}
        print(f"[vLLM-Hook] Timing ({_n_reps} run(s))...")
        for i in range(_n_reps):
            gen_lat, total_lat, peak, art_kb, stats = one_run()
            results["gen_lat"].append(gen_lat)
            results["total_lat"].append(total_lat)
            results["peak_mem"].append(peak)
            results["art_kb"].append(art_kb)
            print(f"  rep {i+1}: gen={gen_lat:.3f}s  total={total_lat:.3f}s  mem={peak:.0f}MB  art={art_kb:.1f}KB")

            # Shape assertions for token_mode sweep
            if token_mode is not None and stats and _hidden_size is not None:
                _assert_hook_shapes(stats, _extract_mode, _hidden_size)

        return results

    finally:
        if _temp_cfg is not None:
            try:
                os.unlink(_temp_cfg)
            except OSError:
                pass


def _assert_hook_shapes(stats, mode, hidden_size):
    """Assert extracted tensor shapes match the extraction mode.

    stats structure: {"hidden_states": {layer_name: [tensor_per_prompt, ...]}}
    """
    if not isinstance(stats, dict):
        return
    hs = stats.get("hidden_states")
    if not isinstance(hs, dict):
        return
    for layer_key, tensor_list in hs.items():
        if not isinstance(tensor_list, list):
            continue
        for i, tensor in enumerate(tensor_list):
            if not isinstance(tensor, torch.Tensor):
                continue
            if mode == "last_token":
                assert tensor.shape == (hidden_size,), (
                    f"last_token: layer {layer_key} prompt {i} shape {tensor.shape} != ({hidden_size},)"
                )
            else:
                assert len(tensor.shape) == 2 and tensor.shape[-1] == hidden_size, (
                    f"all_tokens: layer {layer_key} prompt {i} shape {tensor.shape} "
                    f"expected (seq_len, {hidden_size})"
                )


def run_native_benchmark(dry_run=False, *, layers=None, max_tokens=None, prompts=None):
    print("\n[Native vLLM] Checking availability...")
    try:
        import vllm.distributed.kv_transfer.kv_connector.v1.example_hidden_states_connector  # noqa: F401
    except ImportError as e:
        print(f"[Native vLLM] SKIPPED — ExampleHiddenStatesConnector not available: {e}")
        print("  Upgrade vLLM to >=0.18: pip install 'vllm>=0.18.0'")
        return {"gen_lat": [], "total_lat": [], "peak_mem": [], "art_kb": []}

    from safetensors import safe_open
    from vllm import LLM, SamplingParams

    _layers = layers if layers is not None else TARGET_LAYERS
    _max_tokens = max_tokens if max_tokens is not None else MAX_TOKENS
    _prompts = prompts if prompts is not None else PROMPTS

    print("[Native vLLM] Initializing...")
    tmpdir = tempfile.mkdtemp(prefix="vllm_hs_native_")
    try:
        llm = LLM(
            model=MODEL_ID,
            speculative_config={
                "method": "extract_hidden_states",
                "num_speculative_tokens": 1,
                "draft_model_config": {
                    "hf_config": {
                        "eagle_aux_hidden_state_layer_ids": _layers,
                    }
                },
            },
            kv_transfer_config={
                "kv_connector": "ExampleHiddenStatesConnector",
                "kv_role": "kv_producer",
                "kv_connector_extra_config": {
                    "shared_storage_path": tmpdir,
                },
            },
            dtype="float16",
            enforce_eager=True,
            enable_prefix_caching=False,
            download_dir=DOWNLOAD_DIR,
        )
        sampling_params = SamplingParams(max_tokens=_max_tokens)

        def one_run():
            t0 = time.perf_counter()
            outputs = llm.generate(_prompts, sampling_params)
            t1 = time.perf_counter()
            peak = _gpu_used_mb()

            hs_paths = []
            for out in outputs:
                path = out.kv_transfer_params.get("hidden_states_path")
                assert path is not None, "kv_transfer_params missing 'hidden_states_path'"
                hs_paths.append(path)
                with safe_open(path, framework="pt") as f:
                    _ = f.get_tensor("hidden_states")
            t2 = time.perf_counter()
            art_kb = _files_size_kb(hs_paths)
            return t1 - t0, t2 - t0, peak, art_kb, outputs

        _n_warmup = 0 if dry_run else N_WARMUP
        print(f"[Native vLLM] Warm-up ({_n_warmup} run(s))...")
        for _ in range(_n_warmup):
            one_run()

        _n_reps = 1 if dry_run else N_REPS
        results = {"gen_lat": [], "total_lat": [], "peak_mem": [], "art_kb": []}
        print(f"[Native vLLM] Timing ({_n_reps} run(s))...")
        for i in range(_n_reps):
            gen_lat, total_lat, peak, art_kb, _ = one_run()
            results["gen_lat"].append(gen_lat)
            results["total_lat"].append(total_lat)
            results["peak_mem"].append(peak)
            results["art_kb"].append(art_kb)
            print(f"  rep {i+1}: gen={gen_lat:.3f}s  total={total_lat:.3f}s  mem={peak:.0f}MB  art={art_kb:.1f}KB")

        return results
    finally:
        shutil.rmtree(tmpdir, ignore_errors=True)


