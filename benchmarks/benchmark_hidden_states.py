def _results_to_grid_row(results: dict, layers, prompt_len: int, token_mode: str, system: str) -> dict:
    def _mean(lst):
        return mean(lst) if lst else None
    def _std(lst):
        return stdev(lst) if len(lst) > 1 else (0.0 if lst else None)
    return {
        "layers":         str(layers),
        "prompt_len":     prompt_len,
        "token_mode":     token_mode,
        "system":         system,
        "gen_lat_mean":   _mean(results["gen_lat"]),
        "gen_lat_std":    _std(results["gen_lat"]),
        "total_lat_mean": _mean(results["total_lat"]),
        "total_lat_std":  _std(results["total_lat"]),
        "peak_mem_mean":  _mean(results["peak_mem"]),
        "art_kb_mean":    _mean(results["art_kb"]),
    }


def run_grid_sweep(dry_run: bool, hook_dir, skip_hook: bool, skip_native: bool) -> list:
    """Full 3D grid: layers × prompt_len × token_mode."""
    import itertools
    all_rows = []
    combos = list(itertools.product(
        SWEEP_CONFIGS["layers"],
        SWEEP_CONFIGS["prompt_len"],
        SWEEP_CONFIGS["token_mode"],
    ))
    total = len(combos)
    for i, (layers, prompt_len, token_mode) in enumerate(combos):
        print(f"\n{'─' * 60}")
        print(f"[Grid {i+1}/{total}] layers={len(layers)}  prompt_len={prompt_len}  token_mode={token_mode}")

        hook_results = {"gen_lat": [], "total_lat": [], "peak_mem": [], "art_kb": []}
        native_results = {"gen_lat": [], "total_lat": [], "peak_mem": [], "art_kb": []}

        hook_kwargs  = {"layers": layers, "token_mode": token_mode}
        native_kwargs = {"layers": layers}

        try:
            hook_results, native_results = _run_prompt_len_sweep_point(
                prompt_len, dry_run, hook_dir,
                hook_kwargs, native_kwargs,
                skip_hook,
                skip_native or (token_mode != "last_token"),
            )
        except Exception as e:
            print(f"  ERROR: {e}")

        if not skip_hook:
            all_rows.append(_results_to_grid_row(hook_results, layers, prompt_len, token_mode, "hook"))
        if not skip_native and token_mode == "last_token":
            all_rows.append(_results_to_grid_row(native_results, layers, prompt_len, token_mode, "native"))

    return all_rows


def _results_to_row(results: dict, sweep: str, value, system: str) -> dict:
    """Collapse a results dict into a single CSV row."""
    def _mean(lst):
        return mean(lst) if lst else None

    def _std(lst):
        return stdev(lst) if len(lst) > 1 else (0.0 if lst else None)

    return {
        "sweep": sweep,
        "value": str(value),
        "system": system,
        "gen_lat_mean": _mean(results["gen_lat"]),
        "gen_lat_std":  _std(results["gen_lat"]),
        "total_lat_mean": _mean(results["total_lat"]),
        "total_lat_std":  _std(results["total_lat"]),
        "peak_mem_mean": _mean(results["peak_mem"]),
        "art_kb_mean":   _mean(results["art_kb"]),
    }


def run_sweep(sweep_name: str, values: list, dry_run: bool,
              hook_dir, skip_hook: bool, skip_native: bool,
              base_hook_kwargs: dict, base_native_kwargs: dict) -> list:
    """Run both systems for each value in the sweep. Returns CSV rows."""
    all_rows = []

    for val in values:
        print(f"\n{'─' * 60}")
        print(f"[Sweep:{sweep_name}] value = {val}")

        hook_kwargs = dict(base_hook_kwargs)
        native_kwargs = dict(base_native_kwargs)
        # Per-iteration skip flag — never mutates the outer skip_native parameter.
        _skip_native = skip_native

        if sweep_name == "layers":
            hook_kwargs["layers"] = val
            hook_kwargs["token_mode"] = hook_kwargs.get("token_mode", "last_token")
            native_kwargs["layers"] = val
        elif sweep_name == "prompt_len":
            # Prompts generated lazily after HookLLM is created (need tokenizer)
            hook_kwargs["_prompt_len"] = val
        elif sweep_name == "token_mode":
            hook_kwargs["layers"] = hook_kwargs.get("layers", TARGET_LAYERS)
            hook_kwargs["token_mode"] = val
            # native always stores all prompt tokens — only run for last_token
            # to avoid a duplicate native row.
            _skip_native = _skip_native or (val != "last_token")

        # For prompt_len sweep, prompts are built inside the helper using the
        # HookLLM tokenizer to get accurate token lengths.
        if "_prompt_len" in hook_kwargs:
            target_len = hook_kwargs.pop("_prompt_len")
            hook_results, native_results = _run_prompt_len_sweep_point(
                target_len, dry_run, hook_dir, hook_kwargs, native_kwargs,
                skip_hook, _skip_native
            )
        else:
            if not skip_hook:
                hook_results = run_hook_benchmark(
                    dry_run=dry_run, hook_dir=hook_dir, **hook_kwargs
                )
            else:
                hook_results = {"gen_lat": [], "total_lat": [], "peak_mem": [], "art_kb": []}

            if not _skip_native:
                native_results = run_native_benchmark(dry_run=dry_run, **native_kwargs)
            else:
                native_results = {"gen_lat": [], "total_lat": [], "peak_mem": [], "art_kb": []}

        # For layers sweep, record count not the full list so CSV is plottable.
        csv_val = len(val) if sweep_name == "layers" else val
        if hook_results["gen_lat"]:
            all_rows.append(_results_to_row(hook_results, sweep_name, csv_val, "hook"))
        if native_results["gen_lat"]:
            all_rows.append(_results_to_row(native_results, sweep_name, csv_val, "native"))

    _print_sweep_table(all_rows, sweep_name)
    return all_rows


def _run_prompt_len_sweep_point(target_len, dry_run, hook_dir,
                                hook_kwargs, native_kwargs,
                                skip_hook, skip_native):
    """Handle the prompt_len sweep point where prompts depend on the tokenizer."""
    sys.path.insert(0, str(PROJECT_ROOT / "vllm_hook_plugins"))
    from vllm_hook_plugins import register_plugins
    from vllm_hook_plugins.hook_llm import HookLLM

    register_plugins()

    cfg_path = CONFIG_FILE
    _temp_cfg = None
    layers = hook_kwargs.get("layers")
    token_mode = hook_kwargs.get("token_mode", "last_token")
    if layers is not None or token_mode != "last_token":
        _layers = layers if layers is not None else TARGET_LAYERS
        _temp_cfg = _write_temp_config(CONFIG_FILE, _layers, token_mode)
        cfg_path = _temp_cfg

    # Build prompts before spinning up HookLLM. If hook is skipped we still need
    # prompts for the native benchmark, so use a lightweight HF tokenizer directly.
    from transformers import AutoTokenizer
    tokenizer = AutoTokenizer.from_pretrained(MODEL_ID, cache_dir=DOWNLOAD_DIR)
    prompts = _prompts_for_length(target_len, tokenizer, n=8)
    print(f"  Generated {len(prompts)} prompts of ~{target_len} tokens each")

    hook_results = {"gen_lat": [], "total_lat": [], "peak_mem": [], "art_kb": []}
    try:
        if not skip_hook:
            init_kwargs = dict(
                model=MODEL_ID,
                worker_name="probe_hidden_states",
                analyzer_name="hidden_states",
                config_file=cfg_path,
                download_dir=DOWNLOAD_DIR,
                dtype=torch.float16,
                enable_hook=True,
                enforce_eager=True,
                enable_prefix_caching=False,
            )
            if hook_dir is not None:
                init_kwargs["hook_dir"] = hook_dir

            llm_temp = HookLLM(**init_kwargs)
            _max_tokens = hook_kwargs.get("max_tokens", MAX_TOKENS)
            prefill_params = SamplingParams(temperature=0.0, max_tokens=_max_tokens)
            llm_temp._setup_hooks(cleanup=True)

            use_shm = os.environ.get("VLLM_HOOK_USE_SHM", "0") == "1"
            shm_persist = os.environ.get("VLLM_HOOK_SHM_PERSIST", "0") == "1"
            use_safetensors = os.environ.get("VLLM_HOOK_USE_SAFETENSORS", "0") == "1"
            use_async_save = os.environ.get("VLLM_HOOK_ASYNC_SAVE", "0") == "1"
            _hook_dir = llm_temp._hook_dir

            def one_run_hook():
                one_run_hook._prev_run_id = one_run_hook._last_run_id
                run_id = str(uuid.uuid4())
                one_run_hook._last_run_id = run_id
                with open(llm_temp._run_id_file, "a") as f:
                    f.write(run_id + "\n")
                t0 = time.perf_counter()
                llm_temp.llm.generate(prompts, prefill_params)
                t1 = time.perf_counter()
                stats = llm_temp.analyze(analyzer_spec={"reduce": "none"})
                t2 = time.perf_counter()

                if use_shm and shm_persist:
                    pt = os.path.join(_hook_dir, run_id, "tp_rank_0", "hidden_states.pt")
                    raw = torch.load(pt, map_location="cpu", weights_only=False)
                    peak = raw.get("peak_gpu_mb", _gpu_used_mb())
                    art_kb = None  # filled in after loop
                elif use_shm:
                    peak = stats.get("peak_gpu_mb", _gpu_used_mb())
                    art_kb = None
                elif use_safetensors:
                    art_json_path = os.path.join(_hook_dir, run_id, "tp_rank_0", "hidden_states.json")
                    if not use_async_save:
                        art_st = os.path.join(_hook_dir, run_id, "tp_rank_0", "hidden_states.safetensors")
                        deadline = time.time() + 60.0
                        while time.time() < deadline:
                            if os.path.exists(art_json_path) and os.path.exists(art_st):
                                break
                            time.sleep(0.01)
                        try:
                            with open(art_json_path) as f:
                                peak = json.load(f).get("peak_gpu_mb", _gpu_used_mb())
                        except (FileNotFoundError, KeyError, ValueError):
                            peak = _gpu_used_mb()
                        art_kb = _files_size_kb([art_st, art_json_path])
                    else:
                        peak = _gpu_used_mb()
                        art_kb = None  # filled in after loop for async
                else:
                    pt = os.path.join(_hook_dir, run_id, "tp_rank_0", "hidden_states.pt")
                    raw = torch.load(pt, map_location="cpu", weights_only=False)
                    peak = raw.get("peak_gpu_mb", _gpu_used_mb())
                    art_kb = _files_size_kb([pt])

                return t1 - t0, t2 - t0, peak, art_kb, stats

            _n_warmup = 0 if dry_run else N_WARMUP
            print(f"[vLLM-Hook] Warm-up ({_n_warmup} run(s))...")
            one_run_hook._last_run_id = None
            one_run_hook._prev_run_id = None
            for _ in range(_n_warmup):
                one_run_hook()
            last_warmup_run_id = one_run_hook._last_run_id
            if use_async_save and use_safetensors and last_warmup_run_id:
                # Wait for last warmup artifact to be written before timed reps,
                # so the async queue is drained and size measurements are accurate.
                warmup_json = os.path.join(_hook_dir, last_warmup_run_id, "tp_rank_0", "hidden_states.json")
                warmup_st   = os.path.join(_hook_dir, last_warmup_run_id, "tp_rank_0", "hidden_states.safetensors")
                deadline = time.time() + 60.0
                while time.time() < deadline:
                    if os.path.exists(warmup_json) and os.path.exists(warmup_st):
                        break
                    time.sleep(0.01)
            _n_reps = 1 if dry_run else N_REPS
            hook_results = {"gen_lat": [], "total_lat": [], "peak_mem": [], "art_kb": []}
            print(f"[vLLM-Hook] Timing ({_n_reps} run(s))...")
            for i in range(_n_reps):
                gl, tl, pk, ak, _ = one_run_hook()
                hook_results["gen_lat"].append(gl)
                hook_results["total_lat"].append(tl)
                hook_results["peak_mem"].append(pk)
                hook_results["art_kb"].append(ak)
                print(f"  rep {i+1}: gen={gl:.3f}s  total={tl:.3f}s  mem={pk:.0f}MB  art={ak if ak is not None else 'pending'}")

            # Backfill artifact size. For async save, prefer _prev_run_id (rep N-1,
            # guaranteed written) over _last_run_id (rep N, may not be done yet).
            if use_safetensors and not dry_run:
                try:
                    candidates = []
                    if use_async_save:
                        for cid in [one_run_hook._prev_run_id, one_run_hook._last_run_id]:
                            if cid:
                                candidates.append(cid)
                    else:
                        if one_run_hook._last_run_id:
                            candidates.append(one_run_hook._last_run_id)

                    final_art_kb = None
                    for cand_id in candidates:
                        cand_st   = os.path.join(_hook_dir, cand_id, "tp_rank_0", "hidden_states.safetensors")
                        cand_json = os.path.join(_hook_dir, cand_id, "tp_rank_0", "hidden_states.json")
                        deadline = time.time() + 5.0
                        while time.time() < deadline:
                            if os.path.exists(cand_json) and os.path.exists(cand_st):
                                break
                            time.sleep(0.01)
                        if os.path.exists(cand_json) and os.path.exists(cand_st):
                            final_art_kb = _files_size_kb([cand_st, cand_json])
                            break

                    if final_art_kb is not None:
                        hook_results["art_kb"] = [final_art_kb] * len(hook_results["art_kb"])
                        print(f"  [artifact] size: {final_art_kb:.1f} KB")
                    else:
                        print("  [artifact] WARNING: artifact not found; size not recorded")
                except Exception as _e:
                    print(f"  [artifact] WARNING: could not measure size: {_e}")

    finally:
        if _temp_cfg is not None:
            try:
                os.unlink(_temp_cfg)
            except OSError:
                pass
        try:
            del llm_temp
        except NameError:
            pass
        import gc
        gc.collect()
        torch.cuda.empty_cache()

    if not skip_native:
        native_results = run_native_benchmark(
            dry_run=dry_run, prompts=prompts,
            layers=native_kwargs.get("layers"),
            max_tokens=native_kwargs.get("max_tokens"),
        )
    else:
        native_results = {"gen_lat": [], "total_lat": [], "peak_mem": [], "art_kb": []}

    return hook_results, native_results


